public class Lingkaran {
    double hitungLuas(int r){
        double luas = 22/7 * r * r;
        return luas;
    }
    double hitungLuas(double r){
        double luas = 22/7 * r * r;
        return luas;
    }
    double hitungKelilings(int r){
        double keliling = 22/7 * r * 2;
        return keliling;
    }
    double hitungKeliling(double r){
        double keliling = 22/7 * r * 2;
        return keliling;
    }
}
